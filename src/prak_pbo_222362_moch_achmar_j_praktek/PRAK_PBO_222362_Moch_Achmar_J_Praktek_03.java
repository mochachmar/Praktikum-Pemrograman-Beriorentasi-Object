package prak_pbo_222362_moch_achmar_j_praktek;

public class PRAK_PBO_222362_Moch_Achmar_J_Praktek_03 {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        boolean c;

        c = a && b;
        System.out.println("True && False = " + c);

        c = a || b;
        System.out.println("True || False = " + c);

        c = !a;
        System.out.println("Negasi, !true = " + c);
    }
}
