package prak_pbo_222362_moch_achmar_j_tugas;

public class PRAK_PBO_222362_Moch_Achmar_J_Tugas_01 {
    public static void main(String[] args) {
        int a = 9;
        int b = 62;

        int hasilPenjumlahan = a + b;
        System.out.println("Penjumlahan: " + hasilPenjumlahan);

        int hasilPengurangan = a - b;
        System.out.println("Pengurangan: " + hasilPengurangan);

        int hasilPerkalian = a * b;
        System.out.println("Perkalian: " + hasilPerkalian);

        double hasilPembagian = (double) a / b;
        System.out.println("Pembagian: " + hasilPembagian);

        int hasilModulus = a % b;
        System.out.println("Modulus: " + hasilModulus);
    }
    
}
